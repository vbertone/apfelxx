  //_____________________________________________________________________________
  double psi(double const& t)
  {
    return t * tanh( M_PI * sinh(t) / 2 );
  }

  //_____________________________________________________________________________
  double psip(double const& t)
  {
    return ( M_PI * t * cosh(t) + sinh( M_PI * sinh(t) ) ) / ( 1 + cosh( M_PI * sinh(t) ) );
  }

  //_____________________________________________________________________________
  OgataQuadrature::OgataQuadrature(int const& nu, double const& CutOff, double const& h, int const& nZeroMax):
    _nu(nu),
    _CutOff(CutOff),
    _h(h),
    _nZeroMax(std::min(nZeroMax, (int) jZeros[_nu].size())),
    _xf(_nZeroMax),
    _weights(_nZeroMax)
  {
    // Check that nu is within the available range
    if (_nu < 0 || _nu > (int) jZeros.size())
      throw std::runtime_error(error("OgataQuadrature", "Value on 'nu' not available."));

    // Check that the manimum number of zero's is within the available
    // range, otherwise issue a warning
    if (nZeroMax > (int) jZeros[_nu].size())
      warning("OgataQuadrature", "Maximum mumber of zero's requested larger than those available: setting _nZeroMax to maximum available.");

    // Initialise coordinates and weights
    InitialiseWeights(_nZeroMax);
  }

  //_____________________________________________________________________________
  void OgataQuadrature::InitialiseWeights(int const& nZeroMax)
  {
    // Run over the zeros of J and compute unscaled coordinates and
    // weights. There are 1000 precomputed zeros. This should be
    // enough for all practical applications.
    for (int i = 0; i < nZeroMax; i++)
      {
        const double jz = jZeros[_nu][i];
        const double z  = jz / M_PI;
        _xf[i] = M_PI * psi(_h * z) / _h;
        _weights[i] = M_PI * yn(_nu, jz) * jn(_nu, _xf[i]) * psip(_h * z) / jn(_nu + 1, jz);
      }
  }

  //_____________________________________________________________________________
  template<typename T>
  T OgataQuadrature::transform(std::function<T(double const&)> const& func, double const& qT, bool const& Dynh, int const& nmax, int const& period) const
  {
    // Compute _h dynamically if requested
    if (Dynh)
      {
        OgataQuadrature *ptr =  const_cast<OgataQuadrature*> (this);
        // This seems to be a good and yet simple scaling for _h (but
        // may not be too general).
        ptr -> _h = 0.001 * M_PI * qT;
        ptr -> InitialiseWeights(nmax);
      }

    T integral     = _weights[0] * func(_xf[0] / qT);
    T integralSave = integral;
    int i;
    for (i = 1; i < std::min(nmax, (int) _nZeroMax); i++)
      {
        integral += _weights[i] * func(_xf[i] / qT);

        // Compare the value of the integral across "period"
        // iterations and, if the relative difference is smaller than
        // "_CutOff", break the cycle. A period larger than one is
        // necessary to average out possible oscillations.
        if (i % period == 0)
          {
            if (dabs(1 - dabs(integralSave) / dabs(integral)) < _CutOff)
              break;
            integralSave = integral;
          }
      }
    integral /= qT;

    // If the number of terms is equal to the size of the "jZeros"
    // vector give a warning.
    if (i == (int) _nZeroMax)
      warning("OgataQuadrature", "Number of zero's available exceeded: the integration might not have converged.");

    return integral;
  }

